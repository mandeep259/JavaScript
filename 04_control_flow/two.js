const month = "march"
// switch conditions

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("Febuary");
        break;
    case "march":
        console.log("March");
        break;
    case "april":
        console.log("April");
        break;

    default:
        console.log("Default case match");
        break;
}
